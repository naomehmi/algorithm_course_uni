"""
if-else statement to check whether Mike will graduate on time or not

Mike will graduate on time if he has already finished 7 or 8 semesters in college.
"""

mike = int(input("How many semesters has Mike finished? "))

if mike == 7 or mike == 8:
    print("Mike will graduate on time.")
else:
    print("Mike will not graduate on time")

#complexity = O(1)
